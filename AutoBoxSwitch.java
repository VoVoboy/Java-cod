public class AutoBoxSwitch {
    public static void main(String args[]) {

        Integer iOb = 2;

        switch (iOb) {
            case 1: System.out.println("one");
                break;
            case 2: System.out.println("two");
                break;
            case 3: System.out.println("error");
        }
    }
}
